// Counting
let interview = 0;
let rejected = 0;

// Button1
const interviewCount=document.getElementById('interview')
const rejectCount=document.getElementById('rejected')
const interviewBtn1=document.getElementById('A')
const rejectedBtn1=document.getElementById('B')
const deleteBtn1=document.getElementById('C')

interviewBtn1.addEventListener('click',()=>{
interview++;
interviewCount.innerText=interview;
})
rejectedBtn1.addEventListener('click',()=>{
rejected++;
rejectCount.innerText=rejected;
})
deleteBtn1.addEventListener('click',()=>{
    interview=0;
    rejected=0
    interviewCount.innerText=0;
    rejectCount.innerText=0;
})

